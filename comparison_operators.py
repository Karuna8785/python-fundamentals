# Comparison Operators Practice

x = 15
y = 10

print("x =", x)
print("y =", y)

print("Equal to (==):", x == y)
print("Not equal (!=):", x != y)
print("Greater than (>):", x > y)
print("Less than (<):", x < y)
print("Greater or equal (>=):", x >= y)
print("Less or equal (<=):", x <= y)